package vn.vnpt.vnptit.ecms.dto.idc.khonggian;

public class HanhLangDto {
	private Number hanhLangID;
	private Number phongID;
	private String ten;
	private String tenTat;
	private Number nong;
	private String ghiChu;
	private String moTa;
	private String suDung;

	public Number getHanhLangID() {
		return hanhLangID;
	}

	public void setHanhLangID(Number hanhLangID) {
		this.hanhLangID = hanhLangID;
	}

	public Number getPhongID() {
		return phongID;
	}

	public void setPhongID(Number phongID) {
		this.phongID = phongID;
	}

	public String getTen() {
		return ten;
	}

	public void setTen(String ten) {
		this.ten = ten;
	}

	public String getTenTat() {
		return tenTat;
	}

	public void setTenTat(String tenTat) {
		this.tenTat = tenTat;
	}

	public Number getNong() {
		return nong;
	}

	public void setNong(Number nong) {
		this.nong = nong;
	}

	public String getGhiChu() {
		return ghiChu;
	}

	public void setGhiChu(String ghiChu) {
		this.ghiChu = ghiChu;
	}

	public String getMoTa() {
		return moTa;
	}

	public void setMoTa(String moTa) {
		this.moTa = moTa;
	}

	public String getSuDung() {
		return suDung;
	}

	public void setSuDung(String suDung) {
		this.suDung = suDung;
	}
}
